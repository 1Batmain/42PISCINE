int	ft_str_is_alpha(char *str)
{
	char	c;
	int		i;

	if (*str == '\0')
		return (1);
	i = 0;
	while (str[i])
	{
		c = str[i];
		if ((c < 'A' || c > 'Z') && (c < 'a' || c > 'z'))
			return (0);
		i++;
	}
	return (1);
}
