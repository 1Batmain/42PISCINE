#include <unistd.h>

void	print_result(int *grid)
{
	int		i;
	char	c;

	i = 0;
	while (i < (4 * 4))
	{
		c = '0' + grid[i];
		write(1, &c, 1);
		i++;
		if (i % 4 == 0)
			write(1, "\n", 1);
		else
			write(1, " ", 1);
	}
}

