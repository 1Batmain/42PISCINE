#include "structs.h"

void	paint(t_square s, t_pool p)
{
	int	x;
	int	y;

	x = s.x1;
	y = s.y1;
	while (x < s.x2)
	{
		while (y < s.y2)
		{
			p.grid[y][x] = p.fill;
			y++;
		}
		x++;
	}
}
